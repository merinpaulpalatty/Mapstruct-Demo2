package com.ust.mapstructDemo.vo;

public class StudentVO {
	
	private int id;
	private String name;
	private String markInVO;
	private String city;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMarkInVO() {
		return markInVO;
	}
	public void setMarkInVO(String markInVO) {
		this.markInVO = markInVO;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	

}
